-- 1. Provinces 테이블 (특별시, 광역시 포함한 도)
CREATE TABLE Provinces (
    id INT PRIMARY KEY AUTO_INCREMENT,   -- 행정 구역 고유 ID
    name VARCHAR(100) NOT NULL,           -- 도/특별시/광역시 이름
    description TEXT                      -- 도/특별시/광역시 설명
);

-- 2. Cities 테이블 (시, 군 구분)
CREATE TABLE Cities (
    id INT PRIMARY KEY AUTO_INCREMENT,   -- 시/군 고유 ID
    name VARCHAR(100) NOT NULL,           -- 시/군 이름
    population INT NOT NULL,              -- 인구 수
    area FLOAT NOT NULL,                  -- 면적 (km²)
    description TEXT,                     -- 시/군 설명
    province_id INT,                      -- 속한 도/특별시/광역시 ID
    is_city BOOLEAN,                      -- 시 또는 군 여부 (1: 시, 0: 군)
    FOREIGN KEY (province_id) REFERENCES Provinces(id)  -- 외래 키, Provinces 테이블의 id 참조
);

